    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit(User $user)
    {
        if ($user->id !== Auth::id())
            return redirect()->route('admin.users.index')->with('denied', 'ACCESSO NEGATO. AccessibilitÃ  limitata al tuo account.');
        else
            return view('admin.users.edit', compact('user'));

    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, User $user)
    {
        if ($user->id !== Auth::id())
            return redirect()->route('admin.users.index')->with('denied', 'ACCESSO NEGATO. AccessibilitÃ  limitata al tuo account.');
        else {
            $request->validate([
                'name' => ['string', 'max:45', 'required'],
                'email' => ['email', 'max:255'],
                'surname' => ['string', 'max:45', 'required'],
                'address' => ['string', 'max:255', 'required'],
                'phone_number' => ['string', 'max:12', 'required'],
            ]);
            $user->update($request->all());


            return redirect()->route('admin.users.index')->with('success', 'Profile updated successfully');
        }

    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy(User $user)
    {
        if ($user->id !== Auth::id())
            return redirect()->route('admin.users.index')->with('denied', 'ACCESSO NEGATO. AccessibilitÃ  limitata al tuo account.');
        else {
            $user->delete();
            return redirect()->route('admin.users.index');
        }

    }
